t_p *get_data_from_file(t_file *file, t_it *info_table)
{
	t_p		*table_point;
	int		*z_table;
	
	/* ------------------------------------------------------------------- */
	table_point = NULL;
	/* ------------------------------------------------------------------- */
	file->fd = open(file->name, O_RDONLY);
	if (file->fd == -1)
		return (NULL);
	/* ------------------------------------------------------------------- */
	info_table->max_l = 0;
	info_table->max_c = 0;
	/* ------------------------------------------------------------------- */
	z_table = do_z_table(file, info_table);
	
	/* ------------------------------------------------------------------- */
	close(file->fd);
	return (table_point);
}

