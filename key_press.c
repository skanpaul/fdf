#include "main.h"

/* ************************************************************************** */
int key_press(int key, void *param)
{
	t_data *data;
	data = (t_data *)param;
	/* ----------------------------------------------------- */
	if (key == KEY_C)
	{
		mlx_clear_window(data->mlx_ptr, data->win_ptr);
		draw_grid(100, data, COLOR_BLUE);
	}
	/* ----------------------------------------------------- */
	if (key == KEY_ESC)
	{
		mlx_destroy_window(data->mlx_ptr, data->win_ptr);
		free(data->mlx_ptr);
		exit(0);
	}
	/* ----------------------------------------------------- */
	return (0);
}

