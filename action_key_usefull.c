#include "main.h"

/* ************************************************************************** */
static void	free_malloc(t_p *table_1);

/* ************************************************************************** */
void	action_key_usefull(int key, t_data *data)
{
	if (key == KEY_D)
	{
		init_data(data);
		rendering(data->xyz, data);
	}
	if (key == KEY_ESC)
	{
		mlx_destroy_window(data->mlx_ptr, data->win_ptr);
		free(data->mlx_ptr);
		free_malloc(data->xyz);
		exit(0);
	}
}

/* ************************************************************************** */
static void	free_malloc(t_p *table_1)
{
	if (table_1)
		free(table_1);
}
